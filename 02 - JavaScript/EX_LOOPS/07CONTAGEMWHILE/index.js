/* 7. Contagem de 1 a 10 usando while (Médio) (While Exercise)

Enunciado:
Faça um programa que imprima os números de 1 a 10 usando while.*/

let i = 1;

//  while (i <= 10) → Enquanto i for menor ou igual a 10, o loop continua.
while (i <= 10) {
  console.log(i);
  // i++ → Incrementa i para evitar um loop infinito.
  i++;
}