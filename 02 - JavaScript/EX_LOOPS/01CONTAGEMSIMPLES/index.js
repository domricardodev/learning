/* 1. Contagem de 1 a 10 (Fácil)

Enunciado:
Escreva um programa que imprima os números de 1 a 10 usando um loop for.*/

// for (let i = 1; i <= 10; i++) → Define um loop que começa em 1 (i = 1), vai até 10 (i <= 10), aumentando de 1 em 1 (i++).
for (i = 1; i <= 10; i++) {
  console.log("Numero: " + i)
}
