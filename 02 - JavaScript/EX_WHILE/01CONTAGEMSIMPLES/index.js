/* 1. Contagem de 1 a 10

Enunciado:
Imprima os números de 1 a 10 utilizando o while. */

let cont = 1; // Inicializa a variável 'i' com valor 1

while (cont <= 10) { // Enquanto 'i' for menor ou igual a 10, o loop continuará
  console.log(cont); // Exibe o valor de 'i'
  cont += 1; // Incrementa 'i' em 1 a cada iteração
}