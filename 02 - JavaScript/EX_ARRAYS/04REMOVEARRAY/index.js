/* 4. Remover elementos do array

Enunciado:
Remova o primeiro e o último elemento do array. */


let colors = ["Red", "Blue", "Green", "Yellow", "Grey"]

// Remove o último elemento do array
colors.pop();

// Remove o primeiro elemento do array
colors.shift();

console.log(colors);

// Métodos .pop() (remove o último) e .shift() (remove o primeiro).