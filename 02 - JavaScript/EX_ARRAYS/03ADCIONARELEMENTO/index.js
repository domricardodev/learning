/* 3. Adicionar elementos no array

Enunciado:
Adicione um nome ao final do array e outro no início. */

let fruits = ["Apple", "Banana", "Orange"]


// Adiciona "Grape" no final do array
fruits.push("Grape");


// Adiciona "Mango" no inicio do array
fruits.unshift("Mango")


console.log(fruits);

// Métodos .push() (adiciona no final) e .unshift() (adiciona no início).

