/* 4. Imprimir uma lista de nomes (Nível Fácil)

Enunciado:
Crie um programa que imprima os nomes de uma lista de pessoas. */


// Cria um array com os nomes.
let nomes = ["Mario", "Luigi", "Peach", "Link"]

// O loop começa com i igual a 0 e vai até o comprimento do array (nomes.length).
for (i = 0; i < nomes.length; i++) {
  //  Imprime cada nome na lista, um por vez.
  console.log(nomes[i])
}